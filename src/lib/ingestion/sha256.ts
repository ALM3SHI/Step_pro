/**
 * Dependency-free, synchronous SHA-256.
 *
 * Why not node:crypto — the ingestion pipeline now runs in BOTH places:
 * in the browser for the hybrid Fast-Key workflow (instant, zero-cost),
 * and on the server at insert time. `node:crypto` cannot be bundled for
 * the browser, and Web Crypto's `subtle.digest` is async, which would
 * force `hashQuestion` to become a promise and infect every caller.
 *
 * Why not two implementations — content hashes are the deduplication
 * key. If the client and server computed them differently by even one
 * byte, every question would look new and dedupe would silently stop
 * working. One implementation, used everywhere.
 *
 * Verified byte-identical to node:crypto in scripts/test-sha256.ts.
 */

